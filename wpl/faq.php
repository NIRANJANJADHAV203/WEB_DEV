        1. How can tickets be booked for train travel?<br>
        A: Indian rail currently provides following ways to book tickets:

        Reservation at railway counters
        Online reservation from https://irctc.co.in
        Reservation through registered agents and agencies
        2. How many passengers can be booked in a single ticket/transaction?<br>
        A: A maximum of 6 passenger for general and upto 4 passenger for tatkal.<br>

        3. Can bulk booking be done through IRCTC?<br>
        A: No. Bulk booking cannott be done on the internet.<br>

        4. I would like to transfer my ticket to a friend. What is the process?<br>
        A: The rail ticket can’t be transferred to a friend. It can be transferred to blood relations (including father,
        mother, brother, sister, son, daughter, husband or wife) only. For transfer of ticket, an application must be
        submitted at least 24 hours in advance of the scheduled departure of the train to the station manager/chief
        reservation supervisor.<br>

        5. The name on the ticket is incorrect. How do I get it corrected?<br>
        A: It can’t be done once the ticket is reserved. You need to manually cancel the ticket and rebook again.<br>

        6. How many days before can a reservation be done?<br>
        A: Both the online and offline mode of reservation starts 90 days before the date of journey.<br>

        7. How are payment made for online reservation?<br>
        A: The following modes are available:

        Credit card
        Debit card
        Mobile/Net banking
        EMI/cash card
        8. My e-ticket got accidentally deleted from my mail account. How can I get another copy to be sent to me?<br>
        A: There is no facility to resend the ticket as mail but you can take the print of you ticket anytime. Just
        visit https://irctc.co.in and login to your account. There you go to the booked history section and select the
        ticket and print. You can also enter the PNR number and print the ticket.<br>

        9. What is Tatkal? How can I get a Tatkal ticket?<br>
        A: The Tatkal is a scheme launched by The Indian Railway for passengers who plan their journey at short notice.
        The Tatkal ticket can be booked one day in advance to the train journey, with booking starting at 10 a.m.<br>

        10. What is the cancellation policy? What is the cancellation charge?<br>
        A: A reserved ticket can be cancelled up to the charting of the train. The ticket can be cancelled from the
        cancellation section of the IRCTC page after login. A suitable charge will be deducted for the same. The
        cancellation charges are as follows:

        CANCELLATION TIME CANCELLATION CHARGE
        24 hrs before the departure of the train Rs.120/- for AC First Class/Executive Class, Rs.100/- for AC 2
        Tier/First Class, Rs. 90 for AC 3 Tier/AC Chair car/ AC 3 Economy, Rs.60/- for Sleeper Class and Rs.30/- for
        Second Class.
        Within 24 hrs and up to 4 hours before the scheduled departure of the train 25% of the fare
        Less than 4 hours before the schedule departure of the train up to chart preparation of the train 50% of the
        fare<br>
        11. I cancelled a ticket, but have not been refunded for the amount. How do I proceed?<br>
        A: Generally it takes about 3-7 working days for the refund of the amount. If no refund is made even after 7
        working days, you should contact IRCTC 24×7 Helpline: 011 39340000 or send a mail with the ticketing details to
        care@irctc.co.in .<br>

        12. Is it mandatory to carry a “Proof of Identity” while travelling? What documents can be used as “proof of
        identity”? Will a photocopy be sufficient?<br>
        A: Yes it is mandatory to carry a “Proof of Identity” while travelling in reserved class. The following shall be
        accepted as proof of identity:

        Voter Photo identity card issued by Election Commission of India.
        Passport.
        PAN Card issued by Income Tax Department
        Driving Licence issued by RTO
        Photo identity card having serial number issued by Central/State Government
        Student Identity Card with photograph issued by recognized School/College for their students
        Nationalized Bank Passbook with photograph
        Credit cards issued by banks with laminated photograph
        Unique Identification Card “Aadhaar”.
        Photo identity cards having serial number issued by Public Sector Undertaking of State/Central Government,
        District Administration, Municipal bodies and Penchant Administration
        Only attested photocopy of ration card with photograph and nationalized bank passbook with photographs. No other
        Photocopy will be accepted.<br>

        13. How can I take a print out of the ticket if I have the PNR?<br>
        A: For an E-ticket you can take the re-print of your ticket in your website itself. Click on print section and
        enter your PNR to print.<br>

        14. Does Indian Railway offer any discount for group booking?<br>
        A: No.<br>

        15. Does Indian Railway offer any discount for senior citizens?<br>
        A: Yes.
        Men above 60 yrs: 40% of total amount.
        Women above 58 yrs: 50% of total amount.<br>

        16. How can I reschedule an existing reservation? What is the process and what are the charges?<br>
        A: No, you cannot reschedule any existing reservation. You need to cancel them manually and reserve ticket for
        the other day or for other train.<br>

        17. What is Shatabdi express?<br>
        A: Shatabdi Express trains are a series of fast (called superfast in India) passenger trains operated by Indian
        Railways to connect Metro cities with other cities important for tourism, pilgrimage or business. The Shatabdi
        are among the fastest trains in India and the Indian Railways considers them as prestigious. They have a regular
        speed of about 130 km/h (81 mph). Currently there are 22 shatabdi trains under operation.<br>

        18. Can I take a pet along with me on the train? What is the process?<br>
        A: A passenger can take pet along with him in AC First Class or First Class only, provided he/she reserves
        either a two berth or a four berth compartment exclusively for his/her use, paying the due charges depending
        upon the type of train. Passengers travelling in other classes are not permitted to carry the pet along with
        them. But the pet can be booked and carried in the Luggage/Brake Van paying the charges depending upon the type
        of train. Specially designed Boxes are available in the Brake Van for this purpose. Passengers may contact the
        Parcel Office to book their pet.<br>

        19. I booked tickets but suddenly the web page disconnected and the amount has been deducted from my account. I
        do not have any information if my ticket is booked. Please advise.<br>
        A: In such case first check the booked tickets history in your login. Find whether any link is available in the
        FAILED PAYMENTS. If nothing is there, within 3 working days, your money would be refunded.<br>

        20. I would like to know the name of the passenger against a PNR?<br>
        A: No it is not possible to know the name of a passenger against a PNR. It’s a criminal offence in INDIA.<br>

        21. How to I pay pending amount due to fare increase?<br>
        A: The TTE on-board is going to collect the due amount. Do not forget to collect the receipt for the payment.<br>

        22. What happens to the reservations that did not get confirmed? Will those get cancelled automatically or do I
        need to cancel them manually? Is so within what time frame?<br>
        A: Any unconfirmed ticket will be automatically cancelled and the amount will be refunded to the account through
        which the payment was made. A partially confirmed or RAC ticket needs to be cancelled manually.<br>

        23. I did a bulk booking for my family. Will all of us be allotted together in the same coach during berth
        assignment?<br>
        A: It is not necessary that the entire passenger will be allotted in the same coach. It depends on the
        availability of the seats.<br>

        24. What are RAC tickets? Can I board the train with a RAC ticket?<br>
        A: RAC stands for Reservation Against Cancellation. You will be provided with sitting accommodation initially
        and allotted a berth (in case of passengers not turning up) in the train. You can board the train with an RAC
        ticket.<br>

        25. How to cancel e-ticket after charting?<br>
        A: Once the chart is prepared, cancellation can be done only through TDR (Ticket Deposit Receipt) process.
        Refund sanctioned for TDR will be refunded back to the user account before 90 days.<br>

        26. My ticket is RAC after charting and I don’t and to travel. Will I get the complete refund for cancellation?<br>
        A: In such case the passenger should file a TDR online.<br>

        27. Is partial cancellation available for e-ticket?<br>
        A: Yes. Just go to https://irctc.co.in and visit the booking section. Click on cancel option and select the
        ticket. In the next step you would be asked to select the passengers to cancel the ticket of.<br>

        28. What is the service charge for booking e-ticket?<br>
        A: Rs.10 for booking SL and 2S class and Rs. 20 for rest all classes (1A, 2A, 3A, FC, CC).<br>

        29. Can Reservation be done using Mobile?<br>
        A: Yes using IRCTC mobile (Beta Version).<br>

        30. What is IRCTC mobile?<br>
        A: IRCTC mobile is a unique secure transaction solution for booking ticket through mobile phones. They are
        booked using Mobile Internet. It has a user friendly interface and the payment can be done using Debit card,
        Credit card or mobile banking.
<br>
        31. Where can the station code be found?<br>
        A: User can find station codes from Plan My Travel page of IRCTC. While booking the name of station and station
        code appear automatically in a drop-box after typing 3 alphabets.<br>

        32. Is it safe to provide the credit card number?<br>
        A: IRCTC is a VeriSign certified site, so all the transaction is secured by VeriSign. The passwords are
        transmitted through a 128bit SSL secured path. The site doesn’t store the credit card details. All the
        transactions are handled directly by Payment Gateways.<br>

        33. How secure is my Password?<br>
        A: The Password is transmitted in one way encrypted form and is stored as such. The Password will not be known
        even to IRCTC.<br>

        34. How will I be compensated if I was forced to travel in a lower class due to technical error like A.C failure
        etc?<br>
        A: In such cases a Guard Certificate (GC) should be obtained from train superintendent, TTE, Guard etc and sent
        along with original ticket to:

        Indian Railway Catering & Tourism Corporation Ltd.,
        2nd Floor, STC Building,
        1, Tolstoy Marg,
        New Delhi -110001

        IRCTC will process the case with the Refunds office of the station where the Train originated. After
        verification, the refunds office sanctions a refund which is sent to IRCTC. IRCTC then credits it to your credit
        card / bank account. This process may take up to 6 months for processing.<br>

        35. Are bedrolls given free to passengers travelling in AC Coaches?<br>
        A: Yes bedrolls are provided free of cost to passengers travelling in AC 3, 2, 1 tier and AC first class.<br>

        36. Are catering facilities available in train onboard?<br>
        A. Yes. Most of the trains have pantry car facilities. Catering is also arranged in trains where this facility
        is not available.<br>

        37. Is medical assistance available on trains?<br>
        A: A FIRST AID BOX is available with the Train Guards/Superintendents or Pantry Car Managers. They are also
        trained to provide first aid to passengers. In case of emergency the TTE should be approached so that a message
        is passed to the Next station. In case hospitalization is required, the passenger may have to discontinue his
        journey.<br>

        38. Is there any charge for using the Waiting Room at a station?<br>
        A. Waiting rooms are available at all major stations. They can be used free of cost upon the production of the
        Ticket.<br>

        39. What is the up gradation scheme?<br>
        A. If an accommodation is available in the higher class after chart preparation, the full paying customers are
        upgraded to higher class to fill in the vacant seats for no extra charge.<br>

        40. How can the boarding station be changed?<br>
        A. Yes. You need to visit the nearest station and submit a request to the station master.<br>
